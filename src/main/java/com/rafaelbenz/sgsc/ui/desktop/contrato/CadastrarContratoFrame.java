    private void jTextFieldNomeKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextFieldNomeKeyReleased
        String toCheck = jTextFieldNome.getText();
        int toCheckLen = toCheck.length();
        for (Cliente c : clientesDisponiveis) {
            String data = c.getNome();
            String checkFromClienteNome = "";
            for (int i = 0; i < toCheckLen; i++) {
                if (toCheckLen <= data.length()) {
                    checkFromClienteNome = checkFromClienteNome + data.charAt(i);
                }
            }
            if (checkFromClienteNome.equals(toCheck)) {
                jTextFieldNome.setText(data);
                jTextFieldNome.setSelectionStart(toCheckLen);
                jTextFieldNome.setSelectionEnd(data.length());
                jTextFieldEmail.setText(c.getEmail());
                this.cliente = c;
                break;
            }
        }
    }//GEN-LAST:event_jTextFieldNomeKeyReleased

