    private void jTextFieldServicoKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextFieldServicoKeyReleased
        String toCheck = jTextFieldServico.getText();
        int toCheckLen = toCheck.length();
        for (Servico s : servicosDisponiveis) {
            String data = s.getNome();
            String checkFromClienteNome = "";
            for (int i = 0; i < toCheckLen; i++) {
                if (toCheckLen <= data.length()) {
                    checkFromClienteNome = checkFromClienteNome + data.charAt(i);
                }
            }
            if (checkFromClienteNome.equals(toCheck)) {
                jTextFieldServico.setText(data);
                jTextFieldServico.setSelectionStart(toCheckLen);
                jTextFieldServico.setSelectionEnd(data.length());
                servicoSelecionado = s;
                break;
            }
        }
    }//GEN-LAST:event_jTextFieldServicoKeyReleased

